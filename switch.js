const color = "yellow";

switch (color) {
  case "green":
    console.log("color is green");
    break;
  case "blue":
    console.log("color is blue");
    break;
  case "purple":
    console.log("color is purple");
    break;
  case "red":
    console.log("color is red");
    break;

  case "indigo":
    console.log("color is indigo");
    break;
  case "sky-blue":
    console.log("color is sky-blue");
    break;
  case "olive":
    console.log("color is olive");
    break;
  case "yellow":
    console.log("color is yellow");
    break;
  default:
    console.log("no color is given");
}
